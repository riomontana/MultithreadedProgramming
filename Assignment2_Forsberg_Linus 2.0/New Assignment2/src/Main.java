/**
 * Main class starts the application
 *
 * @author Linus Forsberg
 */

public class Main {

    /**
     * Main method creates new instance of GUI class
     * Runs start-method for creating main frame.
     *
     * @param args
     */
    public static void main(String[] args) {
        GUIMutex gui = new GUIMutex();
        gui.Start();
    }
}
